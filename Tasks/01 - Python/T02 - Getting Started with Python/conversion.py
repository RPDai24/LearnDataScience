# Using int() to convert an Float to Integer
# Using float() to convert an Integer to Float
# Using str() to convert an Integer to String
# Using int() to convert an String to Integer

num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"
# num1 into an integer
num1_int = int(num1)
print("num1: {}".format(num1_int))
# num2 into a float
num2_float = float(num2)
print("num2: {}".format(num2_float))
# num3 into a string
num3_str = str(num3)
print("num3: " + num3_str)
# string1 into an integer
string1_int = int(string1)
print("string1: {}".format(string1_int))
